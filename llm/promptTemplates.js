export function buildUserPromptKorean({ dateTimeKst } = {}) {
  return `
지금은 ${dateTimeKst}이야. 이 시간을 기준으로 지금 가장 뜨거운 경제 토픽 5가지를 아래 형식에 맞춰 요약해줘.

[출력 형식] <제목>

요약 1: (기사 내용 상세 요약)
요약 2: (기사 내용 상세 요약)
요약 3: (기사 내용 상세 요약)

한줄 핵심: (전체 내용을 관통하는 한 문장)

출처: (기사 본문으로 직결되는 실제 URL)

(위 형식을 5번 반복하고, 각 항목 사이에는 구분선을 넣어줘.)
`.trim();
}
